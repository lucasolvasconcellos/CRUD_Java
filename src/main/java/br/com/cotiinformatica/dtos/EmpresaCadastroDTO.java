package br.com.cotiinformatica.dtos;

public class EmpresaCadastroDTO {
	
		
		
		private String nomeFantasia;
		private String razaoSocial;
		private String cnpj;
		private String cnpjConfirmacao;
		
		
		public String getNomeFantasia() {
			return nomeFantasia;
		}
		public void setNomeFantasia(String nomeFantasia) {
			this.nomeFantasia = nomeFantasia;
		}
		public String getRazaoSocial() {
			return razaoSocial;
		}
		public void setRazaoSocial(String razaoSocial) {
			this.razaoSocial = razaoSocial;
		}
		public String getCnpj() {
			return cnpj;
		}
		public void setCnpj(String cnpj) {
			this.cnpj = cnpj;
		}
		public String getCnpjConfirmacao() {
			return cnpjConfirmacao;
		}
		public void setCnpjConfirmacao(String cnpjConfirmacao) {
			this.cnpjConfirmacao = cnpjConfirmacao;
		}
		
		
}
		

	
